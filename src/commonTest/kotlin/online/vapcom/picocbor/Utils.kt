package online.vapcom.picocbor

/**
 * Slow, but pure Kotlin ByteArray to Hex string converter.
 * Used only in tests.
 */
fun ByteArray.toHexString() = joinToString("") { byte ->
    (0xFF and byte.toInt()).toString(16).padStart(2, '0')
}

fun String.decodeHex(): ByteArray {
    check(length % 2 == 0) { "Must have an even length" }

    return chunked(2)
        .map { it.toInt(16).toByte() }
        .toByteArray()
}
